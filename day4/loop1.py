# for 迴圈 (步進迴圈, 一步一步往下做)
# 要印出 1~5
for i in range(1, 6):  # 印出 1~小於6的資料
    print(i, end=" ")
# ---------------------------------------
print()  # 換行
for i in range(1, 6):
    print(i, "Python", end=" ")  # 印出5個 Python 資料
# ---------------------------------------
print()  # 換行
for i in range(1, 10, 2):
    print(i, end=' ')
